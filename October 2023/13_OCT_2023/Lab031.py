#Logical Operators:
# and Logical AND
# or Logical OR
# not Logical NOT

print ("______Following are the Logical Operators ______")

p = True
q = False

print ("______and Logical AND ______")
print(p and q)

print ("______or Logical OR ______")
print(q or p)

print ("______not Logical NOT ______")
print(not p)